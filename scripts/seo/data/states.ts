// Helper to get state by slug
export function getState(slug: string): StateData | undefined {
  return STATES.find(s => s.slug === slug);
}

// Get geographic neighbors that are in our 25-state list
export function getNeighborStates(state: StateData): StateData[] {
  return state.geoNeighbors
    .map(slug => getState(slug))
    .filter((s): s is StateData => s !== undefined);
}
